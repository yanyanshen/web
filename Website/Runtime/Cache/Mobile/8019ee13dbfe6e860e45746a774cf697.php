                    <div class="guide_text">
                        <p>
                            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;目前，根据公安部要求，2016年4月1日起，全国16座城市将试点驾驶证自学直考，这16个城市包括天津、包头、长春、南京、宁波、马鞍山、福州、吉安、青岛、安阳、武汉、南宁、成都、黔东南、大理、宝鸡。
                        </p>

                        <p>
                            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;一、自学直考是指申请小型汽车、小型自动挡汽车驾驶证的人员，使用加装安全辅助装置的自备机动车，在具备安全驾驶经历等条件的人员随车指导下，按照公安机关交通管理部门指定的路线、时间学习驾驶技能，直接申请驾驶证考试。

                        </p>

                        <p>
                            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;二、自学人员应当符合公安部《机动车驾驶证申领和使用规定》规定的申请小型汽车、小型自动挡汽车驾驶证条件，按规定取得学习驾驶证明和学车专用标识后，方可学习场地驾驶和道路驾驶技能。

                        </p>
                        <p>
                            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;三、随车指导人员应当取得相应或者更高准驾车型驾驶证五年以上，没有吸食毒品记录，未发生驾驶机动车造成人员死亡的交通责任事故或者造成人员重伤负主要以上责任的交通事故，没有记满12分或者驾驶证被吊销记录，没有违规随车指导行为记录。

                        </p>
                        <p>
                            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;四、自学用车应为非营运小型汽车或者小型自动挡汽车，在自学直考申请地注册登记，并加装副制动装置、辅助后视镜等安全辅助装置，完成加装后经机动车安全技术检验合格。自学用车应当按规定投保机动车交通事故责任强制保险等相关保险。

                        </p>
                        <p>
                            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;五、自学人员经道路交通安全法律、法规和相关知识考试合格后，应当与随车指导人员一同向户籍地或者居住地的地市级车辆管理所领取学习驾驶证明和学车专 用标识。领取时，应当提交自学人员和随车指导人员身份证明、随车指导人员驾驶证、自学用车所有人身份证明以及登记证书、行驶证、交通事故责任强制保险等凭 证、加装安全辅助装置后的安全技术检验合格证明，并交验自学用车。符合条件的，车辆管理所免费发放学习驾驶证明和学车专用标识。自学人员也可以通过互联网 交通安全综合服务管理平台自行打印或者下载学习驾驶证明。

                        </p>
                        <p>
                            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;六、学车专用标识只允许签注一名自学人员、一名随车指导人员、一辆自学用车。每名随车指导人员、每辆自学用车不得同时签注于2个及2个以上学车专用标 识。已经签注过的随车指导人员或者自学用车，需要培训另一名自学人员的，自上次签注之日起三个月后，方可重新领取学车专用标识。

                        </p>
                        <p>
                            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;七、自学人员学习驾驶中，需要变更随车指导人员、自学用车的，应当向原学车专用标识发放地车辆管理所申请重新领取。变更随车指导人员的，新的随车指导 人员应当同时到场，并提供相关资料凭证；变更自学用车的，应当交验自学用车，并提供相关资料凭证。符合条件的，车辆管理所重新发放学车专用标识，收回原学 车专用标识。自学人员学习驾驶中，学车专用标识遗失、损毁的，应当向原学车专用标识发放地车辆管理所申请补领。

                        </p>
                        <p>
                            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;八、自学人员、随车指导人员或者自学用车所有人提出停止自学申请，自学人员、随车指导人员或者自学用车不符合相关规定条件的，车辆管理所应当注销并收回学车专用标识。未收回学车专用标识的，应当公告作废。

                        </p>
                        <p>
                            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;九、自学人员在道路上学习驾驶技能，应当携带学习驾驶证明，在车身前后放置、粘贴学车专用标识，使用学车专用标识签注的自学用车，在签注的指导人员随 车指导下，按照公安机关交通管理部门指定的路线、时间学习驾驶。正在学习驾驶的自学用车不得搭载随车指导人员以外的其他人员。自学用车暂不用于学习驾驶而 上道路行驶时，应当去除学车专用标识。

                        </p>
                        <p>
                            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;十、自学人员注销学车专用标识后，可以选择在驾驶培训机构学习驾驶。已在驾驶培训机构学习驾驶的人员，可以按照本公告要求申请转为自学驾驶。

                        </p>
                        <p>
                            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;十一、公安机关交通管理部门应当结合实际，在保障安全、减少对交通影响的前提下，指定学习驾驶的路线、时间，在允许学习驾驶的路段起点、终点及沿途设置明显的指示标志，并向社会公告。

                            　　高速公路、城市快速路、学校和医院周边道路不得作为学习驾驶路线。0时至5时、道路交通高峰时段不得作为学习驾驶时间。

                        </p>
                        <p>
                            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;十二、随车指导人员应当参照交通运输部、公安部《机动车驾驶培训教学与考试大纲》的内容和学时要求，指导自学人员学习，真实记录学习过程，监督自学人 员遵守《道路交通安全法》及其实施条例规定的通行规则，保障道路交通安全。随车指导人员不得利用自学用车从事经营性驾驶教学活动。

                        </p>
                        <p>
                            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;十三、自学人员在具备资格的人员随车指导下，使用符合规定的自学用车学习驾驶时有道路交通安全违法行为的，按照《道路交通安全法实施条例》第二十条规定对随车指导人员予以处罚。


                            　　自学人员有未取得学习驾驶证明、学习驾驶证明超过有效期、由不符合规定的人员随车指导等违反规定行为之一的，根据《道路交通安全法》、《机动车驾驶证 申领和使用规定》，按照未取得机动车驾驶证驾驶机动车违法行为，对自学人员处二百元以上二千元以下罚款，可以并处十五日以下拘留；属于将机动车交由自学人 员驾驶的，对行为人处二百元以上二千元以下罚款，可以并处吊销机动车驾驶证。


                            　　随车指导人员利用自学用车从事经营性驾驶教学活动的，由道路运输管理机构按照《道路运输条例》第六十六条规定予以处罚，由车辆管理所注销并收回学车专用标识。

                        </p>
                        <p>
                            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;十四、自学人员在具备资格的人员随车指导下，使用符合规定的自学用车学习驾驶中发生交通事故的，按照《道路交通安全法实施条例》第二十条规定，由随车指导人员承担责任。但自学人员在无具备资格人员随车指导情况下发生交通事故的，自行承担责任。


                            　　自学人员在不通行社会车辆、不属于公众通行的场所学习驾驶发生事故，公安机关交通管理部门接到报案的，参照《道路交通安全法》有关规定办理。

                        </p>
                        <p>
                            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;十五、自学人员按照本公告相关要求，学习驾驶中发生交通事故的，保险公司应当按照保险合同约定予以理赔。

                        </p>
                        <p>
                            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;十六、符合《机动车驾驶证申领和使用规定》规定条件的自学人员，可以向学车专用标识发放地车辆管理所申请机动车驾驶证考试。考试合格的，核发机动车驾驶证。

                        </p>
                        <p>
                            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;十七、本公告自2016年4月1日起施行。

                        </p>
